-- 1) Find the name of the Owner of 'Zen' and 'Indica' cars.

SELECT DISTINCT O.NAME,C.MODEL,C.LICENSE_NO
FROM OWNER AS O
RIGHT JOIN CARS AS C
ON O.LICENSE_NO = C.LICENSE_NO
WHERE C.MODEL IN ('ZEN','Indica');

SELECT NAME
FROM OWNER
WHERE LICENSE_NO IN (
  SELECT LICENSE_NO
  FROM CARS
  WHERE MODEL IN ('ZEN','Indica')
);

