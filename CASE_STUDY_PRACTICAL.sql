-- CASE QUESTION

-- 1) FIND ALL EMPLOYEE WHO HAVE MORE THAN THE AVG SALARY OF EMPLOYEE IN THERIR COMPANY.

SELECT PERSON_NAME,SALARY
FROM WORKS
WHERE SALARY > (
  SELECT AVG(SALARY)
  FROM WORKS
);

-- 2) FIND THE COMPANY WITH LEAST NUMBER OF employees.
SELECT COMPANY_NAME,COUNT(*) AS NO_OF_EMPLOYEE
FROM WORKS
GROUP BY COMPANY_NAME
HAVING COUNT(*) = (
    SELECT MIN(CNT)
    FROM (
        SELECT COUNT(*) AS CNT
        FROM WORKS
        GROUP BY COMPANY_NAME
    ) AS TEMP
);






